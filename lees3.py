s = input('Введите вашу строку: ')

print('Третий символ: ', s[2])
print('Предпоследний символ: ', s[-2])
print('Первые 5 символов: ', s[:5])
print('Вся строка, кроме последних 2х символов: ', s[:-2])
print('Символы с четными индексами: ', s[::2])
print('Символы с нечетными индексами: ', s[1::2])
print('Строка в обратном порядке: ', s[::-1])
print('Символы в обратном порядке через один: ', s[-1::-2])
print('Длина строки: ', len(s))
